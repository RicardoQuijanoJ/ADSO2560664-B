def suma (a,b): #   Se crea un metodo (suma) que recibe 2 parametros (a,b)
    return a+b  #   retorna la suma

def suma(a,b,c):    #   Se crea un metodo (suma) que recibe 3 parametros (a,b,c)
    return a+b+c    #   retorna la suma de los parametros

print(suma(1,2,3))  #   se imprime el llamado al metodo (suma) y se le pasan los 3 datos